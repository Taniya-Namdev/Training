# Membership operators used to test whether a value is a member of a sequence, such as string , list , tuple or dictionary.
# in : Returns True if the specified value is found in sequence .
a = [1,2,4,3,5]
result = 3 in a
print(result)

# not in : Returns True if the specified value is not found in sequence
result2 = 6 not in a
print(result2)