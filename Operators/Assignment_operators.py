# These operators are used to assign values to variables. They also can perform opereations on variables and assign the result back to the variable.

# Assignment (=)
a = 5
print(a)

# Addition Assignment (+=)
a=5
a+= 5
print(a)

# Subtraction Assignment (-=)
a = 5 
a-=2
print(a)

# Multiplication Assignment (*=)
a=5
a*=2
print(a)

# Division Assignment (/=)
a=5
a/=2

# Modulus Assignment (%=)
a = 10
a%=3
print(a)

# Exponentiation Assignment(**)
a=2
a**=3
print(a)
# Floor Division Assignment(//=)
a=10
a//=3
print(a)

